package com.javaex.practice;

public class Ex17 {
	
	public static void main(String[] args) {
		
		int x,y;
		//따옴표로 코딩문장을 마칠 것
		
		
		x=40;
		y=20;
		//따옴표로 코딩문장을 마칠 것
		
		int sum= x+y;
		//변수의 자료형을 지정할 것, 따옴표로 코딩문장을 마칠 것
		
		System.out.println("합은" + sum);
		//출력 문장 안의 쌍따옴표 오류
		
	}

}
